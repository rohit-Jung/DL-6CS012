def check_datatypes():
    # Mutable Data Type Example (List)
    my_list = [1, 2, 3]
    print("Original List:", my_list)
    # Modifying the list (changing contents)
    my_list.append(4)
    print("Modified List:", my_list)
    # Immutable Data Type Example (Tuple)
    my_tuple = (1, 2, 3)
    print("Original Tuple:", my_tuple)
